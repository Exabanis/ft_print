#include "libft.h"

int	ft_strcmp(const char *s1, const char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0' && (t_byte)s1[i] == (t_byte)s2[i])
		i++;
	return ((t_byte)s1[i] - (t_byte)s2[i]);
}
