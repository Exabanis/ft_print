#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int x;

	i = 0;
	x = ft_strlen(s1);
	while (s2[i] != '\0' && i < (int)n)
	{
		s1[x] = s2[i];
		i++;
		x++;
	}
	s1[x] = '\0';
	return (s1);
}
